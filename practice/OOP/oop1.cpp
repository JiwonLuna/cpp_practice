/* 객체란?

객체란, 변수들과 참고 자료들로 이루어진 소프트웨어 덩어리이다.

객체는 자기만의 정보를 나타내는 변수들과, 이를 가지고 어떠한 작업을 하는
함수들로 둘러싸고 있다고 보면 됨. (인스턴스 변수, 인스턴스 메소드)*/

/* 캡슐화(Encapsulation)
객체의 인스턴스 변수에 직접 접근하는 것이 불가능하며
객체의 인스턴스 함수를 통해서 값을 수정하는 것은 가능.

캡슐화 : 외부에서 직접 인스턴스 변수의 값을 바꿀 수 없고 
항상 인스턴스 메소드를 통해서 간접적으로 조절하는 
(극단적으로 불가능한 것은 아님.)

<캡슐화의 장점>
객체가 내부적으로 어떻게작동하는 지 몰라도 사용할 줄 알게됨.
직접적인 명령 뿐만 아니라 함께 일어나는 여러 처리를
직접 해주지 않아도 되도록 해줌.
*/
